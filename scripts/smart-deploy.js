// Fun√ß√£o para processar arquivo com corre√ß√µes inteligentes
function processFileSmart(filePath) {
    try {
        let content = fs.readFileSync(filePath, 'utf8');
        let modified = false;
        let fixesApplied = [];
        
        smartFixes.forEach(fix => {
            const originalContent = content;
            
            if (typeof fix.replacement === 'function') {
                content = content.replace(fix.pattern, fix.replacement);
            } else {
                content = content.replace(fix.pattern, fix.replacement);
            }
            
            if (content !== originalContent) {
                modified = true;
                fixesApplied.push({
                    name: fix.name,
                    critical: fix.critical,
                    description: fix.description
                });
            }
        });
        
        if (modified) {
            fs.writeFileSync(filePath, content, 'utf8');
            return { modified: true, fixes: fixesApplied };
        }
        
        return { modified: false, fixes: [] };
    } catch (error) {
        console.log(`  ‚ùå Erro ao processar ${filePath}: ${error.message}`);
        return { modified: false, fixes: [] };
    }
}

